// Función para obtener el icono apropiado según el label
export const getCustomIconForLabel = (label: string) => {
  const labelLower = label.toLowerCase();
  
  if (labelLower.includes('caja fuerte') || labelLower.includes('guardado')) {
    return <CustomIcons.CajaFuerte />;
  }
  
  if (labelLower.includes('chromecast') || labelLower.includes('controles tv')) {
    return <CustomIcons.ControlesTV />;
  }
  
  if (labelLower.includes('binoculares')) {
    return <CustomIcons.Binoculares />;
  }
  
  if (labelLower.includes('trapo')) {
    return <CustomIcons.Trapo />;
  }
  
  if (labelLower.includes('usb speaker')) {
    return <CustomIcons.USBSpeaker />;
  }
  
  if (labelLower.includes('speaker') && !labelLower.includes('usb')) {
    return <CustomIcons.Speaker />;
  }
  
  if (labelLower.includes('secadora')) {
    return <CustomIcons.Secadora />;
  }
  
  if (labelLower.includes('steamer') || labelLower.includes('plancha a vapor')) {
    return <CustomIcons.Steamer />;
  }
  
  if (labelLower.includes('plancha cabello') || labelLower.includes('plancha de cabello')) {
    return <CustomIcons.PlanchaCabello />;
  }
  
  if (labelLower.includes('cama') || labelLower.includes('ordenada')) {
    return <CustomIcons.Cama />;
  }
  
  if (labelLower.includes('bolso yute')) {
    return <CustomIcons.BolsoYute />;
  }
  
  if (labelLower.includes('bolsa') || labelLower.includes('bolso')) {
    return <CustomIcons.Bolso />;
  }
  
  if (labelLower.includes('sombrero')) {
    return <CustomIcons.Sombrero />;
  }
  
  if (labelLower.includes('bulto')) {
    return <CustomIcons.Bulto />;
  }
  
  if (labelLower.includes('cola de caballo')) {
    return <CustomIcons.ColaCaballo />;
  }
  
  // Icono por defecto
  return <CustomIcons.Default />;
}; 